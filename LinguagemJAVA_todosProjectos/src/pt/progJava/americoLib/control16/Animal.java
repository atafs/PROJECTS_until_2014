package pt.progJava.americoLib.control16;

public class Animal {

	/* attribute */
	private int peso;
	private int idade;

	/* getters and setters */
	public int getPeso() {
		return peso;
	}

	public void setPeso(int peso) {
		this.peso = peso;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}

}
